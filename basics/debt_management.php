    <h3>Strategies for Paying Off Debt</h3>
    
    <h4>1. The Snowball Method</h4>
    <p>Pay off the smallest debt first while making minimum payments on others. This builds momentum and motivation.</p>

    <h4>2. The Avalanche Method</h4>
    <p>Focus on paying the debt with the highest interest rate first, saving you more money in the long run.</p>

    <h4>3. Debt Consolidation</h4>
    <p>Combine multiple debts into a single loan with a lower interest rate for easier management.</p>

    <h3>Tips for Staying Out of Debt</h3>
    <ul>
        <li>Live within your means and avoid unnecessary borrowing.</li>
        <li>Create an emergency fund to prevent reliance on credit.</li>
        <li>Negotiate lower interest rates with lenders.</li>
        <li>Use cash or debit cards to control spending.</li>
    </ul>

